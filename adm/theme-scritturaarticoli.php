  <h2>Istruzioni per scrittura artiicoli</h2>


  <h2>LUNGHEZZA</h2>
  <p>A meno di casi eccezionali, i testi dovrebbero essere lunghi non più di 5000 battute,
    spazi inclusi. Non devono essere neanche troppo corti perché i testi ridotti,
    oltre a rischiare di essere poco esaustivi, sono anche penalizzati in termini
    di indicizzazione</p>


  <h2>VIRGOLETTE</h2>
  <p>Per introdurre i discorsi diretti si usano queste virgolette <span style="color: red;">«»</span>,
    mentre per tutto il resto che deve essere virgolettato (citazioni, titoli di
    libri o film, nomi di progetti, espressioni metaforiche ecc.) si usano queste <span style="color: red;">“ ”</span>.
</p>
  <p>Alla fine dei virgolettati, si chiude con virgoletta e poi il punto <span style="color: red;">».</span>
    (NON viceversa). Nel caso ci siano altri segni (punto esclamativo, interrogativo,
    puntini di sospensione), questi vanno aggiunti prima della virgoletta <span style="color: red;">!».</span>.</p>
  <p>Solo nei titoli e nei sommari è accettabile usare queste <span style="color: red;">“ ”</span> per i discorsi diretti.</p>
  <p>Dopo i due punti, all’inizio di ogni discorso diretto, ci vuole la maiuscola.</p>

  <p>Se all'interno del discorso diretto viene aperto un secondo discorso diretto subordinato,
    per quest'ultimo si usano queste <span style="color: red;">“ ”<span>.</p>


  <h2>TRATTINI</h2>

  <p>Per gli incisi all’interno di una frase si usa il trattino lungo <span style="color: red;">–</span>, con uno spazio prima e uno dopo. Nel caso in cui l’inciso sia alla fine del periodo, non c’è bisogno di usare il trattino di chiusura, basta il punto.</p>

  <p>Dopo il trattino lungo di chiusura dell'inciso non ci vuole la maiuscola.</p>

  <p>Altri segni di punteggiatura vanno messi dopo il trattino lungo di chiusura, senza spazio (per esempio “Crediamo nel nostro progetto – spiega Mauro –, vorremmo che si diffondesse il più possibile...” oppure “Ecco ciò che vogliamo fare – dice Laura –: costruire un modello condiviso...”).</p>

  <p>Per le locuzioni composte (usa-e-getta, nord-ovest, madre-figlio, auto-ristrutturare...) si usa il trattino corto <span style="color: red;">-</span>.</p>


  <h2>NUMERI</h2>

  <p>Generalmente è meglio scrivere i numeri in lettere a meno che non si parli di dati numerici. Per esempio, se si scrive “nel nostro ecovillaggio vivono circa quaranta persone” oppure “per arrivare ci vogliono circa tre ore”, il numero va scritto in lettere.</p>

  <p>Quando si parla di dati precisi, tecnici o scientifici è consentito usare le cifre: “l'impianto ha una potenza di 24 kilowatt”.</p>

  <p>È preferibile usare le cifre anche quando il numero scritto in lettere risulterebbe lungo e ridondante (1500 invece di millecinquecento, 64 miliardi invece di sessantaquattro miliardi, 256 invece di duecentocinquantasei).</p>

  <p>In ogni caso è importante mantenere la coerenza all'interno del testo e non cambiare mai il modo in cui si scrive il numero (per esempio evitare di scrivere “al corso c'erano dieci iscritti” e poi poco più avanti “al corso si sono iscritti in 10”).</p>

  <h2>MAIUSCOLE ACCENTATE</h2>

  <p>Le maiuscole con l’accento NON devono essere lettere semplici apostrofate. Bisogna usare la <span style="color: red;">È</span>, la <span style="color: red;">À</span> ecc., non la E’, la A’ ecc.</p>


  <h2>DESINENZE VOCALI</h2>

  <p>Le desinenze vocali si usano SOLO se la vocale è uguale a quella con cui inizia la parola successiva. Per esempio, se dovete dire “macchine ad acqua” bisogna usare la “d”, mentre se dovete dire “macchine a elettricità” non bisogna usarla.</p>

  <p>Se alcuni di questi caratteri non ci sono sulla vostra tastiera potete digitarli utilizzando il <a href="http://www.oppo.it/tabelle/tabella_ascii.htm">codice ascii</a> digitando ALT più il numero corrispondente sul tastierino numerico. Ecco i codici più usati:</p>


  <ul>
    <li><span style="color: red;">«</span> ALT 174</li>
    <li><span style="color: red;">»</span> ALT 175</li>
    <li><span style="color: red;">È</span> ALT 212</li>
    <li><span style="color: red;">À</span> ALT 183</li>
    <li><span style="color: red;">Ì</span> ALT 0204</li>
    <li><span style="color: red;">Ò</span> ALT 227</li>
    <li><span style="color: red;">Ù</span> ALT 235</li>
    <li><span style="color: red;">–</span> ALT 0150</li>
  </ul>
